import React from "react";
import Form from "./Form";
import { redirect } from "next/navigation";
import { auth } from "@/app/action";
export const metadata = {
  title: "Sign Up",
  description: "Generated by create next app",
};
const page = async () => {
  const checkAuth = await auth();
  if (checkAuth.auth) {
    redirect("/dashboard");
  }
  return <Form />;
};

export default page;
